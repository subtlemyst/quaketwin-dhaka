"""Generate docs/results_summary.md from all phase outputs.

Run after any pipeline re-run so the thesis results chapter draft always
reflects the latest numbers:

    python scripts/build_results_summary.py
"""

from __future__ import annotations

import json
import sys
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))


def _load(path: Path) -> dict | None:
    if not path.exists():
        return None
    return json.loads(path.read_text(encoding="utf-8"))


def main() -> None:
    p2 = _load(ROOT / "outputs/phase2/publish_manifest.json")
    p3 = _load(ROOT / "data/processed/diurnal_exposure_summary.json")
    p4 = _load(ROOT / "data/processed/response_phase4_summary.json")
    ml = _load(ROOT / "data/processed/collapse_model_metrics.json")

    lines: list[str] = []
    add = lines.append

    add("# QuakeTwin Dhaka — Consolidated Results Summary")
    add("")
    add(f"_Auto-generated by `scripts/build_results_summary.py` on "
        f"{datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}. "
        "Do not edit by hand; re-run the script instead._")
    add("")
    add("Scenario: **Mw 7.2, Dauki fault**, ~157 km NE of central Dhaka.")
    add("")

    # ---- Phase 1-2: hazard + buildings -------------------------------
    add("## 1. Seismic hazard (Phase 1)")
    add("")
    if p2 and "hazard_validation" in p2:
        hv = p2["hazard_validation"]
        bed = hv["model_pga_bedrock"]
        amp = hv["model_pga_amplified"]
        mmi = hv["model_mmi"]
        add(f"- Bedrock PGA over the study grid: **{bed['min']:.3f}–{bed['max']:.3f} g** "
            f"(mean {bed['mean']:.3f} g).")
        add(f"- Soil-amplified PGA: **{amp['min']:.3f}–{amp['max']:.3f} g** "
            f"(mean {amp['mean']:.3f} g), driven by the proxy soil-zone map.")
        add(f"- Modelled MMI: **{mmi['min']:.1f}–{mmi['max']:.1f}** (mean {mmi['mean']:.1f}).")
        add("")
        add("Benchmark checks against regional literature:")
        add("")
        add("| Benchmark | Expected range | Model mean | Pass |")
        add("|-----------|----------------|------------|------|")
        for chk in hv.get("benchmark_checks", []):
            lo, hi = chk["expected_range"]
            add(f"| {chk['description']} | {lo}–{hi} | {chk['model_mean']} | "
                f"{'yes' if chk['pass'] else 'NO'} |")
    else:
        add("_Phase 2 manifest missing — run `python scripts/publish_phase2.py`._")
    add("")

    add("## 2. Building vulnerability (Phase 2)")
    add("")
    if p2 and "building_validation" in p2:
        bv = p2["building_validation"]
        add(f"- Buildings analysed: **{bv['buildings']:,}** (OSM footprints, Dhaka bbox).")
        add(f"- Estimated resident population (WorldPop 2020, capacity-capped): "
            f"**{bv['total_population_est']:,}**.")
        add(f"- Mean collapse probability: **{bv['mean_collapse_p']:.3f}**; "
            f"**{bv['high_risk_pct']:.1f}%** of buildings at ≥ 0.5.")
        add("")
        add("Risk by construction type (fragility model):")
        add("")
        add("| Construction type | Buildings | Mean collapse prob. | Population |")
        add("|-------------------|-----------|---------------------|------------|")
        for row in bv.get("by_construction_type", []):
            add(f"| {row['construction_type']} | {row['count']:,} | "
                f"{row['mean_collapse_p']:.3f} | {row['total_population']:,} |")
        add("")
        if ml:
            add(f"The optional XGBoost sensitivity model reproduces fragility labels with "
                f"MAE = {ml['mae']:.3f} on a spatial hold-out; the fragility model remains "
                "the primary thesis result (see methodology §6).")
    else:
        add("_Phase 2 manifest missing._")
    add("")

    # ---- Phase 3 -------------------------------------------------------
    add("## 3. Diurnal population exposure (Phase 3)")
    add("")
    if p3:
        add(f"- Static baseline population: **{p3['baseline_population_static']:,}**.")
        add(f"- Casualty proxy rate: {p3['casualty_rate']} of occupants in collapsed "
            "buildings (injury + fatality proxy).")
        add("")
        conserving = all("redistribution_scale" in r for r in p3["periods"])
        if conserving:
            add("Population is redistributed between buildings by occupancy-specific "
                "diurnal weights and renormalized so the citywide total is conserved "
                "at every hour.")
        else:
            add("**Note:** this summary predates the population-conserving "
                "redistribution fix — re-run `python scripts/run_diurnal_exposure.py`.")
        add("")
        add("| Period | Hour | Total population | High-risk population | Expected casualties |")
        add("|--------|------|------------------|----------------------|---------------------|")
        for r in p3["periods"]:
            add(f"| {r['label']} | {r['hour']:02d}:00 | {r['total_population']:,} | "
                f"{r['high_risk_population']:,} | {r['expected_casualties']:,} |")
        add("")
        worst = max(p3["periods"], key=lambda r: r["expected_casualties"])
        best = min(p3["periods"], key=lambda r: r["expected_casualties"])
        if best["expected_casualties"] > 0:
            ratio = worst["expected_casualties"] / best["expected_casualties"]
            add(f"The worst period is **{worst['label']}** with "
                f"**{worst['expected_casualties']:,}** expected casualties — "
                f"{ratio:.2f}× the best period ({best['label']}).")
    else:
        add("_Phase 3 summary missing — run `python scripts/run_diurnal_exposure.py`._")
    add("")

    # ---- Phase 4 -------------------------------------------------------
    add("## 4. Emergency response (Phase 4)")
    add("")
    if p4:
        add(f"- Modelled period: **{p4['period']}**; hospitals/clinics considered: "
            f"**{p4['hospitals']}**.")
        add(f"- Total expected casualties routed to hospitals: "
            f"**{p4['total_expected_casualties']:,}**.")
        add(f"- Mean response-time proxy: **{p4['mean_response_time_min']:.1f} min** "
            "(straight-line nearest-road travel + blockage delays).")
        add(f"- Hospitals over emergency capacity: **{p4['overloaded_hospitals']}** "
            f"of {p4['hospitals']} (max overload ratio {p4['max_overload_ratio']:.1f}×).")
        add("")
        add("Top rescue-priority buildings:")
        add("")
        add("| Building ID | Collapse prob. | Population | Casualties | Nearest hospital | Priority score |")
        add("|-------------|----------------|------------|------------|------------------|----------------|")
        pop_key = f"pop_{p4['period']}"
        cas_key = f"casualties_{p4['period']}"
        for b in p4.get("top_buildings", [])[:10]:
            add(f"| {b['building_id']} | {b['collapse_probability']:.2f} | "
                f"{b.get(pop_key, '—'):,} | {b.get(cas_key, '—'):,} | "
                f"{b['hospital_name']} | {b['rescue_priority_score']:.3f} |")
    else:
        add("_Phase 4 summary missing — run `python scripts/run_response_model.py`._")
    add("")

    # ---- Limitations ---------------------------------------------------
    add("## 5. Key limitations (carry into thesis Discussion)")
    add("")
    add("1. GMPE is a calibrated regional proxy, not an inverted Bangladesh-specific model; "
        "cite GSHAP / national hazard studies as the benchmark source.")
    add("2. Soil zones and liquefaction indices are literature-based proxies pending "
        "borehole validation.")
    add("3. ~95% of OSM buildings lack construction-type tags and fall back to the "
        "`unknown` fragility class.")
    add("4. Population per building is WorldPop density × footprint with a structural "
        "capacity cap; a census-based dasymetric model would be more accurate.")
    add("5. Diurnal multipliers are scaffold values; calibrate with BBS commuting or "
        "mobile-phone data if available.")
    add("6. Response times use nearest-road straight-line proxies, not network routing; "
        "hospital capacities are inferred from OSM tags.")
    add("")

    out = ROOT / "docs" / "results_summary.md"
    out.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Written: {out}", flush=True)


if __name__ == "__main__":
    main()
